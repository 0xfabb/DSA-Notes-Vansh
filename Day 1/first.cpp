// 1. Simple program to print sum of 2 numbers a and b 


#include <iostream>
using namespace std;
int main()
{
    int a, b;

    cout << "Enter the  value of a \n";
    cin >> a;
    cout << "Enter the value of b \n";
    cin >> b;
    int sum = a + b;
    cout << "The sum of a and b is " << sum << endl;

    return 0;
}